import { createSlice } from '@reduxjs/toolkit';
const settingAccomSlice = createSlice({
    name: 'settingaccom',
    initialState: {
        accom: {
          accomName: null,
            description: null,
            addressDetail: null,
            userId: 0,
            nameHost: null,
            accomCateName: null,
            addressGeneral: null,
            imageAccomsUrls: [],
            acreage: 0,
            numPeople: 0,
            numBathRoom: 0,
            numBedRoom: 0,
            numKitchen: 0,
            numView: 0,
            gradeRate: 0,
            numReview: 0,
            bedRooms: [],
            pricePerNight: 0,
            guide: null,
            refundPolicy: null,
            facilityCategoryList: [],
            surchargeList: [],
        }
    },
    reducers: {
      setAccom(state, action) {
        state.accom = action.payload;
        state.reload = false;
      }
    },
});

export default settingAccomSlice;
